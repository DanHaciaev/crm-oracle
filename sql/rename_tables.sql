-- ============================================================
-- STEP 2: ПЕРЕСОЗДАТЬ SEQUENCES
-- START WITH = LAST_NUMBER + 100 (запас)
-- AGRO_CRM_APP_USERS_SEQ        LAST_NUMBER=5   -> 105
-- CRM_TG_BINDINGS_SEQ  LAST_NUMBER=2   -> 102
-- CRM_APP_USER_EVENTS_SEQ LAST_NUMBER=21 -> 121
-- CRM_CHAT_MESSAGES_SEQ LAST_NUMBER=23  -> 123
-- ============================================================

DROP SEQUENCE AGRO_CRM_APP_USERS_SEQ;
CREATE SEQUENCE AGRO_CRM_AGRO_CRM_APP_USERS_SEQ
  START WITH 105 INCREMENT BY 1 NOCACHE NOCYCLE;

DROP SEQUENCE CRM_TG_BINDINGS_SEQ;
CREATE SEQUENCE AGRO_CRM_TG_BINDINGS_SEQ
  START WITH 102 INCREMENT BY 1 NOCACHE NOCYCLE;

DROP SEQUENCE CRM_APP_USER_EVENTS_SEQ;
CREATE SEQUENCE AGRO_CRM_APP_USER_EVENTS_SEQ
  START WITH 121 INCREMENT BY 1 NOCACHE NOCYCLE;

DROP SEQUENCE CRM_CHAT_MESSAGES_SEQ;
CREATE SEQUENCE AGRO_CRM_CHAT_MESSAGES_SEQ
  START WITH 123 INCREMENT BY 1 NOCACHE NOCYCLE;

-- ============================================================
-- STEP 3: УДАЛИТЬ СТАРЫЕ TRIGGERS, СОЗДАТЬ НОВЫЕ
-- ============================================================

DROP TRIGGER AGRO_CRM_APP_USERS_BI;
DROP TRIGGER CRM_TG_BINDINGS_BI;
DROP TRIGGER CRM_APP_USER_EVENTS_BI;
DROP TRIGGER CRM_CHAT_MESSAGES_BI;

CREATE OR REPLACE TRIGGER AGRO_CRM_AGRO_CRM_APP_USERS_BI
  BEFORE INSERT ON AGRO_CRM_AGRO_CRM_APP_USERS FOR EACH ROW
  WHEN (NEW.ID IS NULL)
BEGIN
  :NEW.ID := AGRO_CRM_AGRO_CRM_APP_USERS_SEQ.NEXTVAL;
END;
/

CREATE OR REPLACE TRIGGER AGRO_CRM_TG_BINDINGS_BI
