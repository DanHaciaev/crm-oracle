/

CREATE OR REPLACE TRIGGER AGRO_CRM_CHAT_MESSAGES_BI
  BEFORE INSERT ON AGRO_CRM_CHAT_MESSAGES FOR EACH ROW
  WHEN (NEW.ID IS NULL)
BEGIN
  :NEW.ID := AGRO_CRM_CHAT_MESSAGES_SEQ.NEXTVAL;
END;
/

